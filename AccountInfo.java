    // Example method to fetch and display user data
    private void loadUserData() {
        if (userEmail == null || userEmail.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No email provided.");
            return;
        }
        try {
            Connection con = DBConnection.getConnection();
            String query = "SELECT * FROM users WHERE email = ?";  // or "WHERE user_id = ?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, userEmail); // or pst.setInt(1, userId);

            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                String firstName = rs.getString("first_name");
                String lastName = rs.getString("last_name");
                String phoneNo = rs.getString("phone_no");
                String email = rs.getString("email");
                String nationality = rs.getString("nationality");
                String password = rs.getString("password");

                // Display data on the Account Info page labels
                frstNametxt.setText(firstName);

                lastNametxt.setText(lastName);
                phonenotxt.setText(phoneNo);
                emailtxt.setText(email);
                nationalitytxt.setText(nationality);
                pswrdtxt.setText(password);  // Display the password
            }

            rs.close();
            pst.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

