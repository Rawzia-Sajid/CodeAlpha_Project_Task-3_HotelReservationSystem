    private void saveUserData() {
        String updatedFirstName = frstNametxt.getText();
        String updatedLastName = lastNametxt.getText();
        String updatedPhoneNo = phonenotxt.getText();
        String updatedEmail = emailtxt.getText();
        String updatedNationality = nationalitytxt.getText();
        String updatedPassword = pswrdtxt.getText();  // Get the updated password

        try {
            Connection con = DBConnection.getConnection();
            String query = "UPDATE users SET first_name = ?, last_name = ?, phone_no = ?, email = ?, nationality = ?, password = ? WHERE email = ?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, updatedFirstName);
            pst.setString(2, updatedLastName);
            pst.setString(3, updatedPhoneNo);
            pst.setString(4, updatedEmail);
            pst.setString(5, updatedNationality);
            pst.setString(6, updatedPassword);
            pst.setString(7, userEmail);  // Use userEmail as the identifier for the update

            int rowsAffected = pst.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "User information updated successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update user information.");
            }

            pst.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

