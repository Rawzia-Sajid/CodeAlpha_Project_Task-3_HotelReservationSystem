     public void setReservationDataToTable() {
        try {
            
            Connection con = DBConnection.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from reservations");

            while (rs.next()) {
                String Id = rs.getString("id");
                String name = rs.getString("customer_name");
                String checkIn = rs.getString("check_in_date");
                String checkOut = rs.getString("check_out_date");
                String roomNo = rs.getString("room_number");
                String roomType = rs.getString("room_type");
                String price = rs.getString("total_price");

                String paymentMethod = rs.getString("payment_method");
                String status = rs.getString("payment_status");

                Object obj[] = {Id, name, checkIn, checkOut, roomNo, roomType, price,
                    paymentMethod, status};
                model = (DefaultTableModel) tblBooking.getModel();
                model.addRow(obj);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }     
    
 
    private void refreshData() {
        // Clear existing data in the table
        DefaultTableModel model = (DefaultTableModel) tblBooking.getModel();
        model.setRowCount(0);

        // Re-fetch and display updated data
        setReservationDataToTable();
    }
   
