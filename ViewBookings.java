    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        String customerName = customerNametxt.getText().trim(); // Get the text entered by the user
        model = (DefaultTableModel) tblBooking.getModel();
        model.setRowCount(0); // Clear existing rows

        String query = "SELECT * FROM reservations";

        // Add a WHERE clause if a name is entered
        if (!customerName.isEmpty()) {
            query += " WHERE customer_name LIKE ?";
        }

        try (Connection con = DBConnection.getConnection(); PreparedStatement pst = con.prepareStatement(query)) {

            // Set the parameter if a name is entered
            if (!customerName.isEmpty()) {
                pst.setString(1, "%" + customerName + "%"); // Use wildcard for partial matches
            }

            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                String Id = rs.getString("id");
                String name = rs.getString("customer_name");
                String checkIn = rs.getString("check_in_date");
                String checkOut = rs.getString("check_out_date");
                String roomNo = rs.getString("room_number");
                String roomType = rs.getString("room_type");
                String price = rs.getString("total_price");

                String paymentMethod = rs.getString("payment_method");
                String status = rs.getString("payment_status");

                Object obj[] = {Id, name, checkIn, checkOut, roomNo, roomType, price, paymentMethod, status};
                model.addRow(obj);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }   
     
    }//GEN-LAST:event_searchbtnActionPerformed

